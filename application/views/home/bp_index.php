<h2>How Educate<span class="red">I</span><span class="yellow">N</span><span class="orange">D</span><span class="green">I</span><span class="blue">A</span>&trade; Works</h2>
</div>
<div class="four columns">
<p>
Teachers can design and deliver their own lessons but can also simply use lessons from our library, which offers a growing selection of lessons designed to meet the needs and interests of all students. Having done it for decades ourselves, we know that teachers don&#8217;t use things as they come out of the box, so our &#8220;Personalize&#8221; button works like a &#8220;Save As,&#8221; so teachers can adjust content to match the reality of their own classrooms without losing rigor. Teachers can also track data on student Common Core State Standards progress by assessing student work with our expert-aligned library.<br/><br />
