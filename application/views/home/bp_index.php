<h2>Benefits</h2>
</div>
<div class="four columns">
<h4 class="red">1. Personalize &#038; Differentiate</h4>
<p>Student needs differ. Personalize our lessons or create your own for online delivery so you can deliver live student support while everyone is learning at their own pace.
</p></div>
<div class="four columns">
<h4 class="blue">2. Track Student &#038; School Progress</h4>
<p>Teaching &#038; learning cannot always be measured by standardized tests alone. By tracking authentic student work, teachers, adminstrators, and parents can see growth.
</p></div>
<div class="four columns">
<h4 class="green">3. Coordinate Instruction Across Grade Levels &#038; Disciplines</h4>
<p>Without a shared language, students find themselves confused as they move across disciplines. CERCA is a shared language that simplifies the core of CCSS.
</p></div>
