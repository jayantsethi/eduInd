<h2>What is Educate<span class="red">I</span><span class="yellow">N</span><span class="orange">D</span><span class="green">I</span><span class="blue">A</span>&trade;?</h2>
</div>
<div class="four columns">
<h3 class="orange center"><img src="http://blog.thinkcerca.com/wp-content/uploads/2013/02/ic_method.png" alt="ic_method" width="189" height="189" class="aligncenter size-full wp-image-68" /><br />
A Method</h3>
<p>Our focus on argumentation as a method for teaching critical thinking is a simple way to increase rigorous engagement with all sorts of texts. Simply by treating all texts as arguments we allow readers to take an analytic approach.
</p></div>
<div class="four columns">
<h3 class="green center"><img src="http://blog.thinkcerca.com/wp-content/uploads/2013/02/ic_platform.png" alt="ic_platform" width="189" height="189" class="aligncenter size-full wp-image-69" /><br />
A Platform</h3>
<p>Our platform allows teachers and students to collaborate more effectively by sharing a common language and set of practices that focus instruction on critical thinking.
</p></div>
<div class="four columns">
<h3 class="blue center"><img src="http://blog.thinkcerca.com/wp-content/uploads/2013/02/ic_library.png" alt="ic_library" width="189" height="189" class="aligncenter size-full wp-image-70" /><br />
A Library</h3>
<p>EducateINDIA&trade; allows teachers to collaborate more easily by providing a simple framework for sharing, using, and personalizing lessons. Our content library is CCSS aligned and engaging for students.
</p></div>
<div class="clearfix"></div>
